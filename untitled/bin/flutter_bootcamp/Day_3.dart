void main() {
  //----------------1 Arithmetic Operators-------------------------

//   Addition (+)
  var a = 10;
  var b = 5;
  print(a + b);

// Subtraction (-)
  print(a - b);

// Multiplication (*)
  print(a * b);

//   Division(/)
  print(a / b);

// Modulus (%)
  print(a % b);

  // ----------------2. Relational (Comparison) Operators-----------

  // Equal to (==)
  print(a == b); // Output: false

  // Not equal to (!=)
  print(a != b); // Output: true

  // Greater than (>)
  print(a > b); // Output: true
  // Less than (<)
  print(a < b); // Output: false

  // Greater than or equal to (>=)
  print(a >= b); // Output: true

  // Less than or equal to (<=)
  print(a <= b); // Output: false

  // ----------------3. Logical Operators-------------

  // Logical AND (&&)
  var x = true;
  var y = false;
  print(x && y); // Output: false

  // Logical OR (||)
  print(x || y); // Output: true

  // Logical NOT (!)
  print(!x); // Output: false

  // ------------------------4. Assignment Operators----------------

  // Simple assignment (=)
  var z = 100;

  // Add and assign (+=)
  z += 5; // Equivalent to z = z + 5;
  print(z); // Output: 15

  // Subtract and assign (-=)
  z -= 3; // Equivalent to z = z - 3;
  print(z); // Output: 12

  // Multiply and assign (*=)
  z *= 2; // Equivalent to z = z * 2;
  print(z); // Output: 24





}
